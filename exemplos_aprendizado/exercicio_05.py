cinco = numeroInt * 5
seis = numeroInt * 6
sete = numeroInt * 7
oito = numeroInt * 8
nove = numeroInt * 9
dez = numeroInt * 10

print(f"A tabuada do numero {numeroInt} é: ")
print(f"{numeroInt} x 0 = {zero}")
print(f"{numeroInt} x 1 = {um}")
print(f"{numeroInt} x 2 = {dois}")
print(f"{numeroInt} x 3 = {tres}")
print(f"{numeroInt} x 4 = {quatro}")
print(f"{numeroInt} x 5 = {cinco}")
print(f"{numeroInt} x 6 = {seis}")
print(f"{numeroInt} x 7 = {sete}")
