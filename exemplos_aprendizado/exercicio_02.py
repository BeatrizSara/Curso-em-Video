""" 
Desafio 02
Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
"""
numero = float(input("Informe um número: "))

dobro = numero * 2 
triplo = numero * 3
raizQ = numero ** (1/2)

print(f"O dobro é {dobro}")
print(f"O triplo é {triplo}")
print(f"A raiz quadrada é {raizQ}")