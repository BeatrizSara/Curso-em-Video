"""
Faça um Programa que peça as 4 notas bimestrais e mostre a média.
"""
bim01 = float(input("Informe a nota do 1º Bimestre: "))
bim02 = float(input("Informe a nota do 2º Bimestre: "))
bim03 = float(input("Informe a nota do 3º Bimestre: "))
bim04 = float(input("Informe a nota do 4º Bimestre: "))

media = (bim01+bim02+bim03+bim04) / 4

print("A média Bimestral é: ", media)
