"""Exemplos simples de Estrutura de repetição FOR"""

# A contagem é de 2 em 2 - chamada ITERAÇÃO ; Contou de 0 ate 7 de 2 em 2.
for c in range (0, 7,  2): 
    print(c)
print("FIM")

# Outro exemplo 
n  = int(input("Informe um número: "))

for c in range(0, n):
    print(c)
print("fim")

